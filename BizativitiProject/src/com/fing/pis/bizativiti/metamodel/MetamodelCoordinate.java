package com.fing.pis.bizativiti.metamodel;

public class MetamodelCoordinate {
    private double x;
    private double y;

    public MetamodelCoordinate(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}